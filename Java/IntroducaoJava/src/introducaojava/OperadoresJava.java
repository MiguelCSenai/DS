package introducaojava;

public class OperadoresJava {
    
    public static void main(String[] args) {
        
        float fValor1 =4, fValor2 = 9;
        System.out.println("SOMA:"+ (fValor1 + fValor2));
        System.out.println("SUBTRACAO: "+ (fValor1 - fValor2));
        System.out.println("MULTIPLICACAO: "+ (fValor1 * fValor2));
        System.out.println("DIVISAO: "+ (fValor1 / fValor2));
        System.out.println("RESTO: "+ (fValor1 % fValor2));
        
    }
    
}
